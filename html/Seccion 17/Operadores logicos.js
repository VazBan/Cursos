//Operadores logicos

// "or || "

/* El operador " or | " va a analizar los valores de la cadena hasta encontrar el primer "true" y una vez que lo haga terminara la ejecucion*/
const OperadorOr = false || true 
console.log(OperadorOr)

// "And &&"
/* El operador "And &&" va  a analizar los valores de la cadena hasta encontrar el primer "false" y una vez que lo haga terminara la ejecucion*/
const OperadorAnd = true && false 
console.log(OperadorAnd)

// "Not !"
/*El operador "Not !" va a cambiar al valor a su opuesto en caso de ser un Boolean ejemplo: El "true" lo va a cambiar a "false"*/
const OperadorNot = !true 
console.log(OperadorNot)