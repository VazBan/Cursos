 
 let numeros = [1,2,3,4,5,6,7,8,9,"mamushca","meme","mamu"]


 /* para acceder a elementos de un arreglo es usando "[]" colocando
  dentro la posicion de nuestro elemento*/

 console.log(numeros[4])

 /* al utilizar " .length " junto con el nombre del arreglo, se puede
 saber cual es el tamano de este y asi su vez usar funciones como la siguiente*/


 for (let i = 0; i < numeros.length; i++) {
    console.log(numeros[i])
 }

 /* Al poner "(numeros[i])" podemos acceder a los elementos de nuestro arreglo [1,2,3,4,5,6,7,8,9,"mamushca","meme","mamu"]*/
